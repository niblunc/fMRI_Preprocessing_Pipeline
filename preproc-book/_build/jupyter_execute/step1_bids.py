# ## What is BIDS?  
# 
# The Brain Imaging Data Structure (BIDS) is a specification for neuroimaging data. The data results can be very complicated and it can be arranged in many ways. BIDS is a standard that arose to address this problem. It is simply a specification for structuring neuroimaging data into a format that is easily readible and understood, so that researchers and collaborators may easily share and contribute their data amongst the community.   
